package com.class7;

public class TaskWhileLoop {
	public static void main (String []args) {
		 
		//print only odd numbers form 50 to 100
		
		int odd = 51;//start with odd number
		while (odd <=100) {
		System.out.println(odd);
		odd+=2;//1st increment is as follows 51+2=53;
	}
//print only even number from 100 to 1
		int even =100;
		while (even >=1) {
			System.out.println(even);
			even -=2;
		}
}}
