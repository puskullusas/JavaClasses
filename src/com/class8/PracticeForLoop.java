package com.class8;

public class PracticeForLoop {
	public static void main(String[] args) {
		// execute with space between numbers
		for (int i = 1; i <= 100; i++) {
			System.out.print(i + " ");
		}
		System.out.println(" ");

		for (int i = 100; i >= 1; i--) {
			System.out.print(i + " ");
		}
		System.out.println(" ");

		for (int i = 20; i >= 2; i -= 2) {
			System.out.print(i + " ");
		}
		System.out.println(" ");
		for (int i=21; i <=51; i+=2) {
			System.out.print(i + " ");
		}
		System.out.println(" ");
		//mod
	
	}
	
}
